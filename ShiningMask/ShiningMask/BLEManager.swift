    // Данные шлются в 960A пакетами по 98 байт: [len][packet_index][R,G,B x pixels...]
    static func imageDataPackets(pixels: [[Bool]], color: (UInt8, UInt8, UInt8)) -> [Data] {
        // Упаковываем пиксели в RGB байты
        var rgb: [UInt8] = []
        for row in pixels {
            for on in row {
                if on {
                    rgb += [color.0, color.1, color.2]
                } else {
                    rgb += [0, 0, 0]
                }
            }
        }
        // Разбиваем на пакеты по 98 байт
        let chunkSize = 98
        var packets: [Data] = []
        var packetIndex: UInt8 = 0
        var offset = 0
        while offset < rgb.count {
            let chunk = Array(rgb[offset..<min(offset + chunkSize, rgb.count)])
            var packet: [UInt8] = [UInt8(chunk.count + 1), packetIndex] + chunk
            packets.append(Data(packet))
            offset += chunkSize
            packetIndex += 1
        }
        return packets
    }
