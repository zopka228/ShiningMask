// ─────────────────────────────────────────────
// MARK: - BLE State
// ─────────────────────────────────────────────
enum BLEState {
    case idle, scanning, connecting, connected, disconnected, error(String)
}

// ─────────────────────────────────────────────
// MARK: - BLEManager
// ─────────────────────────────────────────────
class BLEManager: NSObject, ObservableObject {
    @Published var state: BLEState = .idle
    @Published var discoveredDevices: [CBPeripheral] = []
    @Published var connectedMask: CBPeripheral?
    @Published var signalStrength: Int = 0
    @Published var uploadedImageCount: Int = 0

    private var centralManager: CBCentralManager!
    private var peripheral: CBPeripheral?
    private var cmdCharacteristic: CBCharacteristic?
    private var dataCharacteristic: CBCharacteristic?
    private var rssiTimer: Timer?

    override init() {
        super.init()
        centralManager = CBCentralManager(delegate: self, queue: .main)
    }

    // MARK: - Public API

    func startScanning() {
        guard centralManager.state == .poweredOn else { return }
        discoveredDevices = []
        state = .scanning
        centralManager.scanForPeripherals(withServices: [MaskUUID.service], options: [
            CBCentralManagerScanOptionAllowDuplicatesKey: false
        ])
    }

    func stopScanning() {
        centralManager.stopScan()
        if case .scanning = state { state = .idle }
    }

    func connect(to peripheral: CBPeripheral) {
        self.peripheral = peripheral
        state = .connecting
        centralManager.stopScan()
        centralManager.connect(peripheral, options: nil)
    }

    func disconnect() {
        guard let p = peripheral else { return }
        centralManager.cancelPeripheralConnection(p)
        rssiTimer?.invalidate()
    }

    // MARK: - Команды

    func sendBrightness(_ value: Int) {
        send(MaskCommand.brightness(value), to: cmdCharacteristic)
    }

    func sendAnimation(_ index: Int) {
        send(MaskCommand.animation(index), to: cmdCharacteristic)
    }

    func sendSpeed(_ value: Int) {
        send(MaskCommand.speed(value), to: cmdCharacteristic)
    }

    func sendImage(_ index: Int) {
        send(MaskCommand.image(index), to: cmdCharacteristic)
    }

    func checkImageCount() {
        send(MaskCommand.checkImages, to: cmdCharacteristic)
    }

    /// Отправить кастомное пиксельное изображение
    func sendCustomPixels(_ pixels: [[Bool]], color: (UInt8, UInt8, UInt8)) {
        let packets = MaskCommand.imageDataPackets(pixels: pixels, color: color)
        for (i, packet) in packets.enumerated() {
            DispatchQueue.main.asyncAfter(deadline: .now() + Double(i) * 0.05) {
                self.send(packet, to: self.dataCharacteristic)
            }
        }
    }

    // MARK: - Internal send

    private func send(_ data: Data, to characteristic: CBCharacteristic?) {
        guard let p = peripheral, let ch = characteristic else { return }
        let type: CBCharacteristicWriteType = ch.properties.contains(.writeWithoutResponse) ? .withoutResponse : .withResponse
        p.writeValue(data, for: ch, type: type)
    }

    private func startRSSI() {
        rssiTimer = Timer.scheduledTimer(withTimeInterval: 3, repeats: true) { [weak self] _ in
            self?.peripheral?.readRSSI()
        }
    }
}

// ─────────────────────────────────────────────
// MARK: - CBCentralManagerDelegate
// ─────────────────────────────────────────────
extension BLEManager: CBCentralManagerDelegate {
    func centralManagerDidUpdateState(_ central: CBCentralManager) {
        switch central.state {
        case .poweredOn:    if case .error = state { state = .idle }
        case .poweredOff:   state = .error("Включи Bluetooth")
        case .unauthorized: state = .error("Нет разрешения на Bluetooth")
        default:            state = .error("Bluetooth недоступен")
        }
    }

    func centralManager(_ central: CBCentralManager, didDiscover peripheral: CBPeripheral,
                        advertisementData: [String: Any], rssi RSSI: NSNumber) {
        guard !discoveredDevices.contains(where: { $0.identifier == peripheral.identifier }) else { return }
        discoveredDevices.append(peripheral)
    }

    func centralManager(_ central: CBCentralManager, didConnect peripheral: CBPeripheral) {
        connectedMask = peripheral
        state = .connected
        peripheral.delegate = self
        peripheral.discoverServices([MaskUUID.service])
        startRSSI()
    }

    func centralManager(_ central: CBCentralManager, didFailToConnect peripheral: CBPeripheral, error: Error?) {
        state = .error("Ошибка: \(error?.localizedDescription ?? "?")")
    }

    func centralManager(_ central: CBCentralManager, didDisconnectPeripheral peripheral: CBPeripheral, error: Error?) {
        connectedMask = nil
        cmdCharacteristic = nil
        dataCharacteristic = nil
        rssiTimer?.invalidate()
        state = .disconnected
    }
}

// ─────────────────────────────────────────────
// MARK: - CBPeripheralDelegate
// ─────────────────────────────────────────────
extension BLEManager: CBPeripheralDelegate {
    func peripheral(_ peripheral: CBPeripheral, didDiscoverServices error: Error?) {
        peripheral.services?.forEach {
            peripheral.discoverCharacteristics(nil, for: $0)
        }
    }

    func peripheral(_ peripheral: CBPeripheral, didDiscoverCharacteristicsFor service: CBService, error: Error?) {
        for ch in service.characteristics ?? [] {
            switch ch.uuid {
            case MaskUUID.cmdChar:    cmdCharacteristic = ch
            case MaskUUID.dataChar:   dataCharacteristic = ch
            case MaskUUID.notifyChar: peripheral.setNotifyValue(true, for: ch)
            default: break
            }
        }
    }

    func peripheral(_ peripheral: CBPeripheral, didUpdateValueFor characteristic: CBCharacteristic, error: Error?) {
        guard let data = characteristic.value, data.count > 1 else { return }
        // Ответ CHEC — количество загруженных изображений
        let hex = data.map { String(format: "%02X", $0) }.joined(separator: " ")
        print("Маска ответила: \(hex)")
    }

    func peripheral(_ peripheral: CBPeripheral, didReadRSSI RSSI: NSNumber, error: Error?) {
        DispatchQueue.main.async { self.signalStrength = RSSI.intValue }
    }
}
